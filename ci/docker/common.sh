#!/bin/bash

die() {
  local msg="$1"
  echo -e "$msg"
  exit 1
}

check_yum_environment() {
  which rpm > /dev/null 2>&1 || die "RPM utility is not available"
  which yum > /dev/null 2>&1 || die "Yum ist not available"
}

get_yum_repo_mirror() {
  local mirrorlist_url="$1"
  curl -s "$mirrorlist_url" | grep -e "^http.*" | head -n1 || die "yum mirror retrieval failed"
}

find_or_install_yum_repo_key() {
  local key_path="$1"
  local sink="/dev/null"
  [ -f "$key_path" ] || sink="$key_path"
  cat > $sink || die "failed to write $key_path"
}

